# 基本运算示例
a = 10
b = 3

print(a + b)

print(f"加法: {a} + {b} = {a + b}")
print(f"减法:{a} - {b} = { a - b}")
print(f"乘法:{a} * {b} = {a * b}")
print(f"除法:{a} / {b} = {a / b}")
print(f"取余:{a} % {b} = {a % b}")
print(f"取整:{a} // {b} = {a // b}")
print(f"幂运算:{a} ** {b} = {a ** b}")

# 比较运算
print(f"{a} > {b}: {a > b}")
print(f"{a} < {b}: {a < b}")
print(f"{a} == {b}: {a == b}")
print(f"{a} != {b}: {a != b}")